  /* LEMNISCATE MOTIF — the namesake ∞ */
  function lemniscatePath(cx, cy, a, rot) {
    const steps = 260, pts = [];
    const cs = Math.cos(rot || 0), sn = Math.sin(rot || 0);
    for (let i = 0; i <= steps; i++) {
      const t = (i / steps) * Math.PI * 2;
      const d = 1 + Math.sin(t) * Math.sin(t);
      const x = a * Math.cos(t) / d;
      const y = a * Math.sin(t) * Math.cos(t) / d;
      pts.push((cx + x * cs - y * sn).toFixed(1) + " " + (cy + x * sn + y * cs).toFixed(1));
    }
    return "M " + pts.join(" L ") + " Z";
  }
  function svgEl(name, attrs) {
    const e = document.createElementNS("http://www.w3.org/2000/svg", name);
    for (const k in attrs) e.setAttribute(k, attrs[k]);
    return e;
  }
  let _lem = null;
  function buildLemniscate() {
    const g = document.getElementById("lemniscate");
    if (!g) return;
    const cx = 450, cy = 260, a = 360;
    g.appendChild(svgEl("path", { d: lemniscatePath(cx, cy, a + 34, 0), fill: "none", stroke: "rgba(255,255,255,0.05)", "stroke-width": "1" }));
    g.appendChild(svgEl("path", { d: lemniscatePath(cx, cy, a - 48, 0), fill: "none", stroke: "rgba(56,175,216,0.10)", "stroke-width": "1" }));
    g.appendChild(svgEl("path", { d: lemniscatePath(cx, cy, a, 0), fill: "none", stroke: "rgba(56,175,216,0.30)", "stroke-width": "1.4", "stroke-linecap": "round" }));
    const main = svgEl("path", { d: lemniscatePath(cx, cy, a, 0), fill: "none", stroke: "url(#loopGrad)", "stroke-width": "2.8", "stroke-linecap": "round" });
    g.appendChild(main);
    const L = main.getTotalLength();
    main.style.strokeDasharray = (L * 0.24) + " " + (L * 0.76);
    const tracers = [];
    const N = 3;
    for (let i = 0; i < N; i++) {
      const halo = svgEl("circle", { r: 7, fill: "url(#tracerGrad)" });
      const dot = svgEl("circle", { r: 2.4, fill: "#eaf9ff" });
      g.appendChild(halo); g.appendChild(dot);
      tracers.push({ halo, dot, off: i / N });
    }
    _lem = { main, L, tracers };
    placeTracers(0);
  }
  function placeTracers(t) {
    if (!_lem) return;
    const { main, L, tracers } = _lem;
    tracers.forEach((tr, i) => {
      const p = main.getPointAtLength(((t * 0.9 + tr.off) % 1) * L);
      tr.halo.setAttribute("cx", p.x); tr.halo.setAttribute("cy", p.y);
      tr.dot.setAttribute("cx", p.x); tr.dot.setAttribute("cy", p.y);
      tr.halo.setAttribute("r", (6 + (0.6 + 0.4 * Math.sin(t * 18 + i)) * 3).toFixed(1));
    });
  }
  function animateLemniscate() {
    if (!_lem) return;
    if (window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches) { placeTracers(0.12); return; }
    let t = 0;
    function frame() {
      t += 0.0016;
      _lem.main.style.strokeDashoffset = (-t * _lem.L * 1.1) % _lem.L;
      placeTracers(t);
      requestAnimationFrame(frame);
    }
    requestAnimationFrame(frame);
  }

  /* COUNT-UP */
  function countUp(node) {
    const target = parseFloat(node.dataset.count);
    if (isNaN(target)) return;
    if (window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches) { node.textContent = target; return; }
    const dur = 1100, start = performance.now();
    function step(now) {
      const p = Math.min(1, (now - start) / dur);
      const eased = 1 - Math.pow(1 - p, 3);
      node.textContent = Math.round(target * eased);
      if (p < 1) requestAnimationFrame(step); else node.textContent = target;
    }
    requestAnimationFrame(step);
  }

