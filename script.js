  function setupLifecycle() {
    const root = document.getElementById('lifecycle');
    if (!root) return;
    const lanes = [...root.querySelectorAll('.lc-lane')];
    const stages = [...root.querySelectorAll('.lc-stage')];
    const detail = document.getElementById('lcDetail');
    const cards = [...detail.querySelectorAll('.lc-detail-card')];
    const guide = document.getElementById('lcGuide');
    const pct = (i) => ((i + 0.5) / 6) * 100;
    lanes.forEach(l => {
      const from = +l.dataset.from, to = +l.dataset.to;
      const bar = l.querySelector('.lc-bar');
      bar.style.left = pct(from) + '%';
      bar.style.width = (pct(to) - pct(from)) + '%';
    });

    let selected = 'tune';
    let cardTimer = null;

    function setRail(product) {
      const lane = lanes.find(l => l.dataset.product === product);
      if (!lane) return;
      const from = +lane.dataset.from, to = +lane.dataset.to;
      const color = lane.style.getPropertyValue('--band').trim();
      stages.forEach((s, i) => {
        const lit = i >= from && i <= to;
        const node = s.querySelector('.lc-node');
        const dot = s.querySelector('.lc-node-dot');
        const name = s.querySelector('.lc-stage-name');
        node.style.borderColor = lit ? color : '';
        node.style.background = lit ? color + '2e' : '';
        node.style.boxShadow = lit ? '0 0 14px ' + color + '99' : '';
        node.style.transform = lit ? 'scale(1.14)' : '';
        dot.style.background = lit ? color : '';
        name.style.color = lit ? 'var(--ink)' : '';
      });
      guide.style.setProperty('--gband', color);
      guide.style.left = pct(from) + '%';
      guide.style.width = (pct(to) - pct(from)) + '%';
      guide.classList.add('show');
    }
    function commit(product) {
      clearTimeout(cardTimer);
      selected = product;
      lanes.forEach(l => l.classList.toggle('active', l.dataset.product === product));
      cards.forEach(c => c.classList.toggle('active', c.dataset.product === product));
      setRail(product);
    }
    lanes.forEach(l => {
      const p = l.dataset.product;
      // hover: light the rail instantly, but only swap the (link-bearing) card after a short dwell
      l.addEventListener('mouseenter', () => {
        setRail(p);
        clearTimeout(cardTimer);
        cardTimer = setTimeout(() => commit(p), 200);
      });
      l.addEventListener('mouseleave', () => setRail(selected));
      // click / keyboard: commit immediately
      l.addEventListener('click', () => commit(p));
      l.addEventListener('focus', () => commit(p));
    });
    // entering the detail panel cancels any pending swap so the link stays reachable
    detail.addEventListener('mouseenter', () => { clearTimeout(cardTimer); setRail(selected); });

    commit('tune');
  }

