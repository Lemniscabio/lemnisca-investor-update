  function setupHeroParallax() {
    const hero = document.getElementById("hero");
    const loop = hero && hero.querySelector(".hero-loop");
    if (!loop) return;
    if (window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;
    let tx = 0, ty = 0, rot = 0, cx = 0, cy = 0, crot = 0, raf = null;
    function frame() {
      cx += (tx - cx) * 0.07; cy += (ty - cy) * 0.07; crot += (rot - crot) * 0.07;
      loop.style.transform = `translateY(-50%) translate3d(${cx.toFixed(2)}px, ${cy.toFixed(2)}px, 0) rotate(${crot.toFixed(3)}deg)`;
      if (Math.abs(tx - cx) > 0.1 || Math.abs(ty - cy) > 0.1 || Math.abs(rot - crot) > 0.01) raf = requestAnimationFrame(frame);
      else raf = null;
    }
    function kick() { if (!raf) raf = requestAnimationFrame(frame); }
    hero.addEventListener("pointermove", e => {
      const r = hero.getBoundingClientRect();
      const dx = (e.clientX - r.left) / r.width - 0.5;
      const dy = (e.clientY - r.top) / r.height - 0.5;
      tx = dx * 46; ty = dy * 30; rot = dx * 4;
      kick();
    });
    hero.addEventListener("pointerleave", () => { tx = 0; ty = 0; rot = 0; kick(); });
  }

